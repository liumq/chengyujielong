const seen = new Set(existingWords);
let addedCount = 0;
let skippedCount = 0;

for (const item of newIdioms) {
  if (!seen.has(item.word)) {
    seen.add(item.word);
    merged.push(item);
    addedCount++;
  } else {
    skippedCount++;
  }
}

// 验证数据完整性
let errors = 0;
for (const item of merged) {
  if (!item.word || !item.pinyin || !item.first || !item.last || !item.meaning) {
    console.error(`数据不完整: ${item.word}`);
    errors++;
  }
}

// 统计首字拼音分布
const firstPinyinMap = {};
for (const item of merged) {
  firstPinyinMap[item.first] = (firstPinyinMap[item.first] || 0) + 1;
}

// 统计末字拼音分布
const lastPinyinMap = {};
for (const item of merged) {
  lastPinyinMap[item.last] = (lastPinyinMap[item.last] || 0) + 1;
}

